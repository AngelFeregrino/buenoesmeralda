    private void jPanelDescuentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelDescuentoMouseClicked
        SalidaFinal.mostrartotal = "0";
        for (int i = 0; i < jTableArticulos.getRowCount(); i++) {
            String precio = (String) jTableArticulos.getValueAt(i, 5);

            double precioint = Double.parseDouble(precio);

            double decimal = Integer.parseInt(jTextFieldDescuento.getText());

            double decimaldescuento = decimal / 100;

            double descuento = (precioint - (precioint * decimaldescuento));

            int descuentoint = (int) descuento;
            String descuentostring = String.valueOf(descuentoint);

            jTableArticulos.setValueAt(descuentostring, i, 5);

        }
        for (int i = 0; i < jTableArticulos.getRowCount(); i++) {

            String precio = (String) jTableArticulos.getValueAt(i, 5);
            int precioint = Integer.parseInt(precio);
            int totalmente = Integer.parseInt(SalidaFinal.mostrartotal);
            totalmente = totalmente + precioint;

            SalidaFinal.mostrartotal = String.valueOf(totalmente);
        }

        jLabelTotal.setText("$ " + SalidaFinal.mostrartotal);

        evt.consume();

    }//GEN-LAST:event_jPanelDescuentoMouseClicked

