    private void jPanelAceptar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelAceptar1MouseClicked

        int fila = jTableArticulos.getRowCount();
        for (int i = 0; i < fila; i++) {

            try {
                
                String ProductoID = (String) jTableArticulos.getValueAt(i, 0);
                String Fecha = (String) jTableArticulos.getValueAt(i, 6);
                String preciototal = (String) jTableArticulos.getValueAt(i, 5);
                String Cantidad = (String) jTableArticulos.getValueAt(i, 4);

                String q = "INSERT INTO `salida`(`id_salida`, `salida_producto_id`, `salida_fecha`, `salida_cantidad`) VALUES ('NULL','" + ProductoID + "','" + Fecha + "','" + Cantidad + "')";
                String sumaS = "SELECT SUM(entrada) FROM caja;";
                String restaS = "SELECT SUM(salida) FROM caja;";
                Conexion.ejecutar(q);
                int preciototalint = Integer.parseInt(preciototal);
                int suma = Conexion.ejecutar2(sumaS);
                int resta = Conexion.ejecutar2(restaS);
                int totalsql = preciototalint + suma;
                int totalfinal = totalsql - resta;
                String total2 = String.valueOf(preciototalint);
                String totalfinal2 = String.valueOf(totalfinal);
                String cajaentrada = "INSERT INTO `caja`(`id_transaccion`, `entrada`, `salida`, `total`, `descripcion`)"
                        + "VALUES ('NULL','" + total2 + "','0','" + totalfinal2 + "','Ventas')";
                Conexion.ejecutar(cajaentrada);

            } catch (SQLException ex) {
                Logger.getLogger(VentanaVentaFinal.class.getName()).log(Level.SEVERE, null, ex);
            }
            DefaultTableModel dm = (DefaultTableModel) SalidaFinal.jTable1.getModel();
            while (dm.getRowCount() > 0) {
                dm.removeRow(0);
            }

            dispose();
        }
    }//GEN-LAST:event_jPanelAceptar1MouseClicked

