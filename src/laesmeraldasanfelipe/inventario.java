    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        javax.swing.JFileChooser jF1 = new javax.swing.JFileChooser();
        String barras = jTextField5.getText();
        String id = jTextField1.getText();
        String ruta = "";

        try {

            if (jF1.showSaveDialog(null) == jF1.APPROVE_OPTION) {

                ruta = jF1.getSelectedFile().getAbsolutePath();

                //Aqui ya tiens la ruta,,,ahora puedes crear un fichero n esa ruta y escribir lo k kieras...
            }

        } catch (Exception ex) {

            ex.printStackTrace();

        }

        try {
//creacion del tamaÃ±o de la pagina para el codigo de barras
            Rectangle pageSize = new Rectangle(175, 65);
            
            
            Document doc = new Document(pageSize, 8, 8, 8, 8);

            
            
            String obtenerprecio="SELECT precio from producto WHERE producto_id="+id;
        try {
            precio=Conexion.ejecutarprecio(obtenerprecio);
        } catch (SQLException ex) {
            Logger.getLogger(inventario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String nombre=jTextField2.getText();
        String palabra=(nombre+": $"+precio);
            
           // centerprecio.setAlignment(Element.ALIGN_RIGHT);
          // centerprecio.setFont(font);
          //  doc.add(centerprecio);
            
            
           
            
            PdfWriter pdf = PdfWriter.getInstance(doc, new FileOutputStream(ruta + ".pdf"));
            doc.open();
            Barcode128 code = new Barcode128();

            code.setCode(barras);
            Image img = code.createImageWithBarcode(pdf.getDirectContent(), BaseColor.BLACK, BaseColor.BLACK);
            img.scalePercent(75);
            
            
            Font tamano = new Font();
            tamano.setSize(8);
            
            Paragraph p;
            p = new Paragraph(palabra, tamano);
            doc.add(img);
            doc.add(p);
                    
                   
            
         

            doc.close();
            precio=null;

        } catch (FileNotFoundException ex) {
            Logger.getLogger(inventario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(inventario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

